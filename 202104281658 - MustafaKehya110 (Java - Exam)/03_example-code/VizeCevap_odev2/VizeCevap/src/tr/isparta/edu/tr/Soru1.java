    private void jBGirisDosyasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGirisDosyasiActionPerformed
        JFileChooser dosyaSec = new JFileChooser();

        int result = dosyaSec.showOpenDialog(this);
        String yol = "";
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = dosyaSec.getSelectedFile();
            yol = selectedFile.getAbsolutePath();
        }
        if (yol != "") {
            try {
                Scanner scanner = new Scanner(new File(yol));
                while (scanner.hasNextLine()) {
                    String[] satir = scanner.nextLine().split("   ");
                    for (int i = 0; i < satir.length; i++) {
                        if (i % 2 == 0) {
                            tamSayilar.add(Integer.valueOf(satir[i]));
                        } else {
                            ondalikliSayilar.add(Double.valueOf(satir[i]));
                        }
                    }
                }
              
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Soru1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBGirisDosyasiActionPerformed

