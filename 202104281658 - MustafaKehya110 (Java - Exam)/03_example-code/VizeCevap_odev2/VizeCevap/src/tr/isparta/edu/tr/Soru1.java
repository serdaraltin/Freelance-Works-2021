    private void jBAktarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAktarActionPerformed
     
        try {
            FileWriter yaz = new FileWriter(ondalikliSayilarYol);
            PrintWriter printWriter = new PrintWriter(yaz);
            for (int i = 0; i < ondalikliSayilar.size(); i++) {
                if(i > 0 && i % 3 == 0)
                    printWriter.print("\n");
                 printWriter.print(String.valueOf(ondalikliSayilar.get(i)+"   "));
            }
            printWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(Soru1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            FileWriter yaz = new FileWriter(tamSyilarYol);
             PrintWriter printWriter = new PrintWriter(yaz);
            for (int i = 0; i < tamSayilar.size(); i++) {
                if(i > 0 && i % 3 == 0)
                     printWriter.print("\n");
                 printWriter.print(String.valueOf(tamSayilar.get(i)+"   "));
            }
            printWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(Soru1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(this, "Aktarım tamamlandı");
    }//GEN-LAST:event_jBAktarActionPerformed

